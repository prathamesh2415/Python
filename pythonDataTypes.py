# Arithmetic Operators
a = 10
b = 5
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
print("Floor Division:", a // b)

# Comparison Operators
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater than or equal to:", a >= b)
print("Less than or equal to:", a <= b)

# Logical Operators
x = True
y = False
print("Logical AND:", x and y)
print("Logical OR:", x or y)
print("Logical NOT:", not x)

# Bitwise Operators
print("Bitwise AND:", a & b)
print("Bitwise OR:", a | b)
print("Bitwise XOR:", a ^ b)
print("Bitwise NOT:", ~a)
print("Bitwise left shift:", a << 1)
print("Bitwise right shift:", a >> 1)

# Assignment Operators
c = 10
print("c :", c)
c += 5
print("c += 5:", c)
c -= 3
print("c -= 3:", c)
c *= 2
print("c *= 2:", c)
c /= 4
print("c /= 4:", c)
c %= 3
print("c %= 3:", c)
c **= 2
print("c **= 2:", c)
c //= 2
print("c //= 2:", c)

# Identity Operators
d = a
print("d is a:", d is a)
print("d is not b:", d is not b)

# Membership Operators
list1 = [1, 2, 3, 4, 5]
print("3 in list1:", 3 in list1)
print("6 not in list1:", 6 not in list1)