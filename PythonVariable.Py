# Integer variable
age = 25
print("Age:", age)

# Float variable
height = 5.9
print("Height:", height)

# String variable
name = "John Doe"
print("Name:", name)

# Boolean variable
is_student = True
print("Is student:", is_student)

# List variable
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Tuple variable
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Dictionary variable
person = {
    "name": "Alice",
    "age": 30,
    "is_student": False
}
print("Person:", person)

# Set variable
unique_numbers = {1, 2, 3, 4, 5}
print("Unique numbers:", unique_numbers)

# NoneType variable
nothing = None
print("Nothing:", nothing)

print("Hello World")